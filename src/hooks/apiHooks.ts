import type {MediaItem, MediaItemWithOwner, UserWithNoPassword, Like, Comment} from 'hybrid-types/DBTypes';
import {useCallback, useEffect, useState} from 'react';
import {fetchData} from '../Utils/fetch-data';
//import type {Credentials, RegisterCredentials} from '../Utilis/types/localTypes';
import type { Credentials, RegisterCredentials } from '../Utils/types/localTypes';
import type {AvailableResponse, LoginResponse, MediaResponse, UploadResponse, MessageResponse, UserResponse} from 'hybrid-types/MessageTypes';

import {File} from 'expo-file-system';
import {useUpdateContext} from './ContextHooks';
import AsyncStorage from '@react-native-async-storage/async-storage';

const useMedia = (instance = false, ownFiles = false) => {
  const [mediaArray, setMediaArray] = useState<MediaItemWithOwner[]>([]);
  const {update} = useUpdateContext();
  // const {getMediaByTagName} = useTag();

  useEffect(() => {
    const getMedia = async () => {
      if (!instance) {
        return;
      }
      try {
        const endpoint = ownFiles ? '/media/bytoken' : '/media';

        const options = {
          method: 'GET',
          headers: {
            Authorization: 'Bearer ' + (await AsyncStorage.getItem('token')),
          },
        };

        const media = await fetchData<MediaItem[]>(
          process.env.EXPO_PUBLIC_MEDIA_API + endpoint,
          options,
        );

        // const media = await getMediaByTagName('defrtyhjuiklo');

        const mediaWithOwners = await Promise.all<MediaItemWithOwner>(
          media.map(async (item) => {
            try {
              const owner = await fetchData<UserWithNoPassword>(
                `${process.env.EXPO_PUBLIC_AUTH_API}/users/${item.user_id}`,
              );
              const mediaItemWithOwner: MediaItemWithOwner = {
                ...item,
                username: owner.username,
              };
              return mediaItemWithOwner;
            } catch (error) {
              console.error(error);
              return {
                ...item,
                username: 'not found',
              };
            }
          }),
        );
        setMediaArray(mediaWithOwners);
      } catch (error) {
        console.error(error);
      }
    };

    getMedia();
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [update]);

  const postMedia = async (
    file: UploadResponse,
    inputs: Record<string, string>,
    token: string,
  ) => {
    // create a suitable object for Media API,
    // the type is MediaItem without media_id, user_id, thumbnail, screenshots, and created_at. All those are generated by the API.
    const mediaData = {
      ...file.data,
      ...inputs,
    };
    const options = {
      method: 'POST',
      headers: {
        Authorization: 'Bearer ' + token,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(mediaData),
    };
    // post the data to Media API and get the data as MediaResponse
    return fetchData<MediaResponse>(
      process.env.EXPO_PUBLIC_MEDIA_API + '/media',
      options,
    );
  };

  return {mediaArray, postMedia};
};

const useAuthentication = () => {
  const postLogin = async (inputs: Credentials) => {
    const fetchOptions = {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(inputs),
    };
    const loginResult = await fetchData<LoginResponse>(
      process.env.EXPO_PUBLIC_AUTH_API + '/auth/login',
      fetchOptions,
    );
    // console.log(loginResult);
    return loginResult;
  };

  return {postLogin};
};

const useUser = () => {
  const resourceUrl = process.env.EXPO_PUBLIC_AUTH_API + '/users';

  const postRegister = async (inputs: RegisterCredentials) => {
    const fetchOptions = {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(inputs),
    };
    const registerResult = await fetchData(resourceUrl, fetchOptions);
    return registerResult;
  };

  const getUserByToken = useCallback(
    async (token: string) => {
      const options = {
        method: 'GET',
        headers: {
          Authorization: 'Bearer ' + token,
        },
      };
      return fetchData<UserResponse>(resourceUrl + '/token', options);
    },
    [resourceUrl],
  );

  const getUsernameAvailable = async (username: string) => {
    // fetch from endpoint /users/username/:username
    return fetchData<AvailableResponse>(`${resourceUrl}/username/${username}`);
  };

  const getEmailAvailable = async (email: string) => {
    // fetch from endpoint /users/email/:email
    return fetchData<AvailableResponse>(`${resourceUrl}/email/${email}`);
  };

  return {
    postRegister,
    getUserByToken,
    getUsernameAvailable,
    getEmailAvailable,
  };
};

const useFile = () => {
  const [loading, setLoading] = useState(false);
  const postFile = async (file: File, token: string) => {
    // create FormData object
    const formData = new FormData();
    // add file to FormData
    formData.append('file', file);
    // upload the file to file server and get the file data
    // return the file data. The type is UploadResponse
    const options = {
      method: 'POST',
      headers: {
        Authorization: 'Bearer ' + token,
      },
      body: formData,
    };
    const result = await fetchData<UploadResponse>(
      process.env.EXPO_PUBLIC_UPLOAD_API + '/upload',
      options,
    );
    return result;
  };

  return {postFile, loading, setLoading};
};

